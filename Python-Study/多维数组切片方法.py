import numpy as np

b = np.arange(24)
a = b.reshape((2, 3, 4))
print('原多维数组')
print(a)
print('--------------------')
print('数组a第0个元素（二维数组）下的所有子元素（一维数组）的第一列')
print(a[0, :, 0])
print('--------------------')
print('取所有二维数组下的每个二维数组的第0个元素（一维数组）')
print(a[:, 0])
print('--------------------')
print('取所有二维数组下面的所有一维数组的每一个一维数组的第0个元素')
print(a[:, :, 0])
print('--------------------')
print('取所有二维数组下面的所有一维数组的每一个一维数组的第0到2（不包含第2）个元素')
print(a[:, :, 0:2])
